export function add(a: number, b: number): number {
    return a + b;
}

export function subtract(a: number, b: number): number {
    return a - b;
}
console.log("-------------------");
console.log("Add : ", add(1, 2)); // 3
console.log("Subtract: ", subtract(1, 2)); // -1
console.log("-------------------");
